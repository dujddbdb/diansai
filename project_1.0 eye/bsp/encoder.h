
// @file    encoder.h
// @brief   双路正交编码器驱动 (TIM2+TIM4编码器模式)
// @note    左编码器: TIM2_CH1(PA0)+CH2(PA1) — 对应A路电机/TB6612左轮
//          右编码器: TIM4_CH1(PD12)+CH2(PD13) — 对应B路电机/TB6612右轮
//          编码器模式TI12, 4倍频计数, 16位计数器(0~65535)
//          每次读取后清零, 得到增量脉冲(带方向符号)

#ifndef __ENCODER_H__
#define __ENCODER_H__

#include "stm32f4xx.h"

/**************************** 用户可配置区域 ***************************/

// 编码器输入捕获滤波值
// 用于消除编码器信号的抖动干扰，取值范围0x0~0xF
// 0xF为最强15级滤波，抗干扰能力最强，但会略微增加信号延迟
#define ENCODER_IC_FILTER   0xF

// 定时器16位自动重载值（计数器最大值）
// 16位定时器最大计数值为65535，计数器从0计数到此值后自动清零
// 由于使用编码器模式，计数值会根据旋转方向递增或递减
#define ENCODER_PERIOD      65535

// 左轮编码器累计脉冲数（有符号16位整数）
// 正数 = 正转（前进方向），负数 = 反转（后退方向）
// 每次调用Encoder_ReadAll()后更新此值，计数器清零后重新计数
extern volatile int16_t Encoder_Left;

// 右轮编码器累计脉冲数（有符号16位整数）
// 正数 = 正转（前进方向），负数 = 反转（后退方向）
// 每次调用Encoder_ReadAll()后更新此值，计数器清零后重新计数
extern volatile int16_t Encoder_Right;

// 双路编码器初始化
// 功能：配置TIM2和TIM4为正交编码器接口模式，配置GPIO、时钟、输入捕获滤波
//       编码器模式为TI12（两路输入都计数），实现4倍频计数
//       配置完成后自动启动计数器开始计数
// 参数：无
// 返回值：无
void Encoder_Init(void);

// 读取双路编码器增量脉冲并清零计数器
// 功能：读取TIM2和TIM4的当前计数值，转换为有符号的增量脉冲数
//       结果存入全局变量Encoder_Left和Encoder_Right中
//       读取完成后将两个定时器的计数器清零，为下一次计数做准备
//       本函数应定期调用（如每隔固定时间调用一次）以获取速度信息
// 参数：无
// 返回值：无
void Encoder_ReadAll(void);

#endif
