
// @file    tb6612.h
// @brief   TB6612FNG双路直流有刷电机驱动接口
// @note    硬件: TIM1高级定时器PWM + GPIO方向控制
//          A路(左轮): PE10=AIN1, PE8=AIN2, PE9=PWMA(TIM1_CH1)
//          B路(右轮): PE12=BIN1, PE15=BIN2, PE11=PWMB(TIM1_CH2)
//          TB6612内部自带死区保护(<1us), 无需软件插入死区
//
//          PWM频率 = 168MHz / PRE / PER
//          示例: PRE=84, PER=1000 → f_PWM = 168MHz/84/1000 = 2kHz
//          高级定时器必须使能MOE(主输出), 否则PWM引脚无输出

#ifndef __TB6612_H__
#define __TB6612_H__

#include "stm32f4xx.h"

/**************************** 用户可配置区域 ***************************/

// PWM频率参数计算公式: f_PWM = 168MHz / TB6612_PRE / TB6612_PER
// 推荐配置：
//   2kHz  (PRE=84, PER=1000) — 默认值，扭矩充足但有轻微噪音
//   10kHz (PRE=84, PER=200)  — 安静运行，扭矩略有下降
//   20kHz (PRE=84, PER=100)  — 静音模式（超出人耳听觉范围），扭矩进一步降低

// TIM1预分频器值（Prescaler）
// 定时器时钟 = 168MHz / (PRE + 1)，此处PRE=84对应2MHz定时器时钟
#define TB6612_PRE   84

// TIM1自动重装载值（Period），即PWM周期
// PWM频率 = 定时器时钟 / (PER + 1)
// 此值也决定了速度控制的分辨率，值越大分辨率越高
#define TB6612_PER   1000

// 速度参数上限（等于PWM周期值，对应100%占空比）
// 电机速度参数范围: 0 ~ TB6612_MAX_SPEED
#define TB6612_MAX_SPEED  TB6612_PER

/**************************** 函数声明 ********************************/

// TB6612电机驱动初始化
// 功能：配置GPIO方向控制引脚（推挽输出）和TIM1 PWM输出通道
//       配置TIM1为PWM模式1，使能高级定时器主输出（MOE位）
//       初始化后电机处于停止状态（PWM占空比为0）
// 参数：
//   pre - 定时器预分频器值，控制PWM频率的分子部分
//   per - 定时器自动重装载值，即PWM周期，控制PWM频率的分母部分
// 返回值：无
void TB6612_Init(uint16_t pre, uint16_t per);

// A路电机（左轮）控制
// 功能：控制A路直流电机的旋转方向和转速
//       通过GPIO控制AIN1/AIN2设置方向，通过PWM占空比调节转速
// 参数：
//   dir   - 旋转方向，0 = 正转（前进），1 = 反转（后退）
//   speed - PWM比较值（转速），范围 0 ~ TB6612_PER，值越大转速越快
// 返回值：无
void TB6612_ACtrl(uint8_t dir, uint32_t speed);

// B路电机（右轮）控制
// 功能：控制B路直流电机的旋转方向和转速
//       通过GPIO控制BIN1/BIN2设置方向，通过PWM占空比调节转速
// 参数：
//   dir   - 旋转方向，0 = 正转（前进），1 = 反转（后退）
//   speed - PWM比较值（转速），范围 0 ~ TB6612_PER，值越大转速越快
// 返回值：无
void TB6612_BCtrl(uint8_t dir, uint32_t speed);

// 双路电机同时控制（整车运动控制）
// 功能：同时控制A路和B路电机的方向和转速
//       适用于差速驱动小车的运动控制，可实现前进、后退、转弯等动作
// 参数：
//   a_dir   - A路电机（左轮）旋转方向，0 = 正转，1 = 反转
//   a_speed - A路电机（左轮）转速，范围 0 ~ TB6612_PER
//   b_dir   - B路电机（右轮）旋转方向，0 = 正转，1 = 反转
//   b_speed - B路电机（右轮）转速，范围 0 ~ TB6612_PER
// 返回值：无
void TB6612_CarCtrl(uint8_t a_dir, uint32_t a_speed,
                     uint8_t b_dir, uint32_t b_speed);

// 双路有符号速度控制（差分驱动，最常用的上层接口）
// 功能：通过有符号整数同时控制左右轮的速度和方向
//       正数 = 前进，负数 = 后退，绝对值 = 速度大小
//       内部自动根据速度符号设置方向，速度绝对值限制在TB6612_MAX_SPEED以内
// 参数：
//   left  - 左轮速度（有符号），≥0 = 前进，<0 = 后退，绝对值为转速值
//   right - 右轮速度（有符号），≥0 = 前进，<0 = 后退，绝对值为转速值
// 返回值：无
void TB6612_SetSpeed(int left, int right);

#endif
